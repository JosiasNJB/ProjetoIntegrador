		<h3>Yby</h3>
		
		<p>
		Yby significa "o chão que se pisa" na língua Tupi Guarani. Acreditamos que escolher um nome originário da nossa terra apresente um ato simbólico e potente.<br>
		Desde a chegada dos invasores em 1500, os nativos dessa terra sofrem em conjunto com povos africanos o extermínio físico e cultural.<br>
		Depois de vários séculos, formou-se uma sociedade baseada no controle de poucos sob muitos. O modelo educacional insuficiente, a alimentação precária e as forças de segurança a fim de suprimir qualquer revolta são essenciais para que a constituição não seja cumprida.<br>
		Durante o ano de 2022 apresentou-se a nós por meio desse projeto, a oportunidade de retornar socialmente todo o conhecimento adquirido em 3 anos de muita resistência.<br>

